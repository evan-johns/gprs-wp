            </h2>
            <div>
               You cannot avoid what you cannot see. Ground Penetrating Radar (GPR) is
               a non-invasive locating tool that allows our SIM Certified Project Managers
               to scan post-tension concrete for conduit, rebar, and other encased objects,
               and to effectively locate utility lines, water and sewer pipes, telecom
               and fiberoptic cables, and subsurface voids.
